    public void IdOtomatis() throws SQLException{
        Connection connection = null;
        try{
            connection = koneksi.getConnection();
            Statement st= connection.createStatement();
            String sql_id="SELECT * FROM anggota order by anggota_id desc";
            ResultSet rs=st.executeQuery(sql_id);
            if(rs.next())
            {
                String anggota_id=rs.getString("anggota_id").substring(1);
                String AN=""+(Integer.parseInt(anggota_id)+1);
                String No1="";
                if (AN.length()==1)
                {
                    No1="000";
                } else if (AN.length()==2)
                {
                    No1="00";
                } else if (AN.length()==3)
                {
                    No1="0";
                }
                txtAnggotaId.setText("A"+No1+AN);
            }else 
            {
                txtAnggotaId.setText("A0001");
            }
          }catch(Exception e) {
              JOptionPane.showMessageDialog(null, e);
          }
    }
    
// Mengambil data dari tabel anggota
public void loadDataAnggota() {
    Connection connection = null;
    try {
        connection = koneksi.getConnection();
        Object header[] = {"Anggota ID", "Nama", "Email", "No Telepon", "Alamat", "Pekerjaan"};
        DefaultTableModel data = new DefaultTableModel(null, header);
        tabelAnggota.setModel(data);
        String sql_data = "SELECT * FROM anggota";

        PreparedStatement pstmt = connection.prepareStatement(sql_data);
        ResultSet rs = pstmt.executeQuery();
        while (rs.next()) {
            String d1 = rs.getString(1);
            String d2 = rs.getString(2);
            String d3 = rs.getString(3);
            String d4 = rs.getString(4);
            String d5 = rs.getString(5);
            String d6 = rs.getString(6);

            String d[] = {d1, d2, d3, d4, d5, d6};
            data.addRow(d);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    } finally {
        if (connection != null) {
            try {
                connection.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
}
    
