    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
    String p_anggota_id = txtAnggotaId.getText();
    // Panggil store procedure
    try (
            // Mendapatkan koneksi dari kelas DatabaseConnection
        Connection connection = koneksi.getConnection()) {
        // Panggil store procedure
        String storedProcedureCall = "{call delete_data_anggota(?)}";
        try (CallableStatement callableStatement = connection.prepareCall(storedProcedureCall)) {
            // Set parameter stored procedure
            callableStatement.setString(1, p_anggota_id);
            callableStatement.execute();
            JOptionPane.showMessageDialog(this, "Data Berhasil di Delete!!");
            txtAnggotaId.setText("");
            txtNama.setText("");
            txtEmail.setText("");
            txtTelepon.setText("");
	    txtAlamat.setText("");
	    txtPekerjaan.setText("");

	loadDataAnggota();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Data Gagal di Delete!!");
    }
    }//GEN-LAST:event_hapusActionPerformed

