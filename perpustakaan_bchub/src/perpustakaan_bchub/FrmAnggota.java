    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        // TODO add your handling code here:
        // Mengisi nilai untuk parameter store procedure
    String p_anggota_id = txtAnggotaId.getText();
    String p_nama = txtNama.getText(); // Memperbaiki pengambilan nama
    String p_email = txtEmail.getText();
    String p_telepon = txtTelepon.getText();
    String p_alamat = txtAlamat.getText();
    String p_pekerjaan = txtPekerjaan.getText();
    
    try {
        // Mendapatkan koneksi dari kelas DatabaseConnection
        Connection connection = koneksi.getConnection();
        // Panggil stored procedure
        String storedProcedureCall = "{call insert_data_anggota (?, ?, ?, ?, ?, ?)}"; // Menggunakan kurung kurawal
        try (CallableStatement callableStatement = connection.prepareCall(storedProcedureCall)) {
            // Set parameter stored procedure
            callableStatement.setString(1, p_anggota_id);
            callableStatement.setString(2, p_nama);
            callableStatement.setString(3, p_email);
            callableStatement.setString(4, p_telepon);
            callableStatement.setString(5, p_alamat);
            callableStatement.setString(6, p_pekerjaan);
            callableStatement.execute();
            JOptionPane.showMessageDialog(this, "Data Berhasil di Input!!");
            
            // Mengosongkan field input
            txtAnggotaId.setText("");
            txtNama.setText(""); // Memperbaiki pengosongan nama
            txtEmail.setText("");
            txtTelepon.setText("");
            txtAlamat.setText("");
            txtPekerjaan.setText("");
            
            loadDataAnggota();
        }
        connection.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Data Gagal di Input!!");
        e.printStackTrace();
    }
    }//GEN-LAST:event_inputActionPerformed

