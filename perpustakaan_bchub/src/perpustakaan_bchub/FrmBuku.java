public void loadDataBuku() {
    Connection connection = null;
    try {
        connection = koneksi.getConnection();
        Object header[] = {"Buku ID", "Judul", "Penulis", "Penerbit", "Tahun Terbit", "ISBN", "Salinan Tersedia"};
        DefaultTableModel data = new DefaultTableModel(null, header);
        TabelBuku1.setModel(data);
        String sql_data = "SELECT * FROM buku";

        PreparedStatement pstmt = connection.prepareStatement(sql_data);
        ResultSet rs = pstmt.executeQuery();
        while (rs.next()) {
            String d1 = rs.getString(1); // Buku ID
            String d2 = rs.getString(2); // Judul
            String d3 = rs.getString(3); // Penulis
            String d4 = rs.getString(4); // Penerbit
            String d5 = rs.getString(5); // Tahun Terbit
            String d6 = rs.getString(6); // ISBN
            String d7 = rs.getString(7); 
            // Jika Anda ingin mengonversi d7 ke String, Anda bisa melakukannya di sini

            String d[] = {d1, d2, d3, d4, d5, d6, d7}; // Menggunakan d7String
            data.addRow(d);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);
    } finally {
        if (connection != null) {
            try {
                connection.close();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
}

    public void IdOtomatis(){
        Connection connection = null;
        try{
            connection = koneksi.getConnection();
            Statement st= connection.createStatement();
            String sql_id="SELECT * FROM buku order by buku_id desc";
            ResultSet rs=st.executeQuery(sql_id);
            if(rs.next())
            {
                String buku_id=rs.getString("buku_id").substring(1);
                String AN=""+(Integer.parseInt(buku_id)+1);
                String No1="";
                if (AN.length()==1)
                {
                    No1="000";
                } else if (AN.length()==2)
                {
                    No1="00";
                } else if (AN.length()==3)
                {
                    No1="0";
                }
                txtBukuId.setText("B"+No1+AN);
            }else 
            {
                txtBukuId.setText("B0001");
            }
          }catch(Exception e) {
              JOptionPane.showMessageDialog(null, e);
          }
    }
    
    
