    private void inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inputActionPerformed
        // TODO add your handling code here:
    String p_buku_id = txtBukuId.getText();
    String p_judul = txtJudul.getText(); // Memperbaiki pengambilan nama
    String p_penulis = txtPenulis.getText();
    String p_penerbit = txtPenerbit.getText();
    String p_tahun_terbit = txtTahunTerbit.getText();
    String p_isbn = txtKategori.getText();
    String p_salinan = txtSalinan.getText();
    
    
    try {
        // Mendapatkan koneksi dari kelas DatabaseConnection
        Connection connection = koneksi.getConnection();
        // Panggil stored procedure
        
        String storedProcedureCall = "{call insert_data_buku (?, ?, ?, ?, ?, ?, ?)}"; // Menggunakan kurung kurawal
        try (CallableStatement callableStatement = connection.prepareCall(storedProcedureCall)) {
            // Set parameter stored procedure
            callableStatement.setString(1, p_buku_id);
            callableStatement.setString(2, p_judul);
            callableStatement.setString(3, p_penulis);
            callableStatement.setString(4, p_penerbit);
            callableStatement.setString(5, p_tahun_terbit);
            callableStatement.setString(6, p_isbn);
            callableStatement.setString(7, p_salinan);
            callableStatement.execute();
            JOptionPane.showMessageDialog(this, "Data Berhasil di Input!!");
            
            // Mengosongkan field input
            txtBukuId.setText("");
            txtJudul.setText(""); // Memperbaiki pengosongan nama
            txtPenulis.setText("");
            txtPenerbit.setText("");
            txtTahunTerbit.setText("");
            txtKategori.setText("");
            txtSalinan.setText("");
            
            loadDataBuku();
        }
        connection.close();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Data Gagal di Input!!");
        e.printStackTrace();
    }
    }//GEN-LAST:event_inputActionPerformed

